#include <unistd.h>

int	main(int av, char **ac)
{
	int	i;
	int	n;

	i = 1;
	n = 0;
	while (ac[i] != 0)
	{
		while (ac[i][n] != '\0')
		{
			write(1, &ac[i][n], 1);
			n++;
		}
		n = 0;
		write(1, "\n", 1);
		i++;
	}
	return (av);
}
