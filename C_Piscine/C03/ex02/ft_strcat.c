char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	k;

	k = 0;
	while (dest[k] != '\0')
	{	
		k++;
	}
	i = 0;
	while (src[i] != '\0')
	{
		dest[k] = src[i];
		i++;
		k++;
	}
	dest[k] = '\0';
	return (dest);
}
